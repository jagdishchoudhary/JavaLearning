package Codechef;
import java.util.*;
class KitchenTimings {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int testCases = sc.nextInt();
        while (testCases-- != 0){
            int X = sc.nextInt();
            int Y = sc.nextInt();
            System.out.println(Y-X);
        }
    }
}
